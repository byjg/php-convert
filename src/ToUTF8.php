    /**
     * Base convert based on array
     *
     * @param array $vector
     * @param string $text
     * @return string
     */
    protected static function baseConversion(array $vector, string $text): string
    {
        foreach ($vector as $key => $value) {
            $char = $value;
            if (is_array($value)) {
                $char = "";
                foreach ($value as $ascii) {
                    $char .= chr($ascii);
                }
            }
            $text = str_replace($key, $char, $text);
        }

        return $text;
    }


    /**
     * Base conversion
     *
     * @param string[] $vector
     * @param string $text
     * @return string
     */
    protected static function baseConversion($vector, $text)
    {
        $result = "";
        $lenText = strlen($text);
        $keys = array_keys($vector);
        for ($i = 0; $i < $lenText; $i++) {
            if (ord($text[$i]) == 226) {
                $first = ord($text[$i++]);
                $second = ord($text[$i++]);
                $result .=
                    isset($vector[$first][$second][ord($text[$i])])
                        ? $vector[$first][$second][ord($text[$i])]
                        : '?'
                ;
            } elseif (in_array(ord($text[$i]), $keys)) {
                $first = ord($text[$i++]);
                $result .= isset($vector[$first][ord($text[$i])]) ? $vector[$first][ord($text[$i])] : '?';
            } else {
                $result .= $text[$i];
            }
        }

        return $result;
    }
